public void showCbbVoucher(ArrayList<Voucher> list) {
        cbbVoucher.removeAllItems();
        ArrayList<String> listcbbVC = new ArrayList<>();
        for (Voucher voucher : list) {
            listcbbVC.add(voucher.getTen());
        }
        for (String string : listcbbVC) {
            cbbVoucher.addItem(string);
        }
    }

    public void ShowgioHang(ArrayList<HoaDonChiTiet_RePones> list) {
        dtmGioHang = (DefaultTableModel) tbGioHang.getModel();
        dtmGioHang.setRowCount(0);
        for (HoaDonChiTiet_RePones hoaDonChiTiet_RePones : list) {
            dtmGioHang.addRow(new Object[]{
                hoaDonChiTiet_RePones.getIDSanPhamChiTiet(), hoaDonChiTiet_RePones.getTensp(), hoaDonChiTiet_RePones.getSoLuong(), hoaDonChiTiet_RePones.getDonGia()
            });
        }
    }

    public void showHoaDon(ArrayList<HoaDon_Repones> list) {
        dtmHoaDon = (DefaultTableModel) tbHoaDon.getModel();
        dtmHoaDon.setRowCount(0);
        for (HoaDon_Repones hoaDon_Repones : list) {
            dtmHoaDon.addRow(new Object[]{
                hoaDon_Repones.getIdHoaDon(), hoaDon_Repones.getNgayTao()
            });
        }
    }

    public void showSanPham(ArrayList<SanPhamChiTiet_Repones> list) {
        dtmSanPham = (DefaultTableModel) tbSanPham.getModel();
        dtmSanPham.setRowCount(0);
        for (SanPhamChiTiet_Repones sanPhamChiTiet_Repones : list) {
            dtmSanPham.addRow(new Object[]{
                sanPhamChiTiet_Repones.getIdSanPhamChitiet(), sanPhamChiTiet_Repones.getTen(), sanPhamChiTiet_Repones.getMauSac(), sanPhamChiTiet_Repones.getChatLieu(), sanPhamChiTiet_Repones.getKichThuoc(), sanPhamChiTiet_Repones.getThuongHieu(), sanPhamChiTiet_Repones.getSoLuongConLai(), sanPhamChiTiet_Repones.getGiaTien()
            });
        }
    }

    public int tongTien() {
        int tongTien = 0;
        for (int i = 0; i < tbGioHang.getRowCount(); i++) {
            int soLuongMua = Integer.parseInt(tbGioHang.getValueAt(i, 2).toString());
            int donGia = Integer.parseInt(tbGioHang.getValueAt(i, 3).toString());
            tongTien += (donGia * soLuongMua);
        }
        return tongTien;
    }

    public void tinhToanTien(int tongTien) {
        int tienDuocGiam = tongTien * (Integer.parseInt(txtPhanTramGiam.getText())) / 100;
        txtTienDuocGiam.setText(String.valueOf(tienDuocGiam));
        int tienPhaiTra = tongTien - tienDuocGiam;
        txtTienPhaiTra.setText(String.valueOf(tienPhaiTra));
    }

    public boolean checkChuoi(String chuoi) {
        try {
            Integer.parseInt(chuoi);
            return true;
        } catch (Exception e) {
            return false;
        }
    }

    public void TraHang(int idHoaDon, int idSanPham, int soLuong) {
        int soLuongHangTon = qlySPCT.getSanPhambyID(idSanPham).get(0).getSoLuongConLai();
        int soLuongUpdate = soLuongHangTon + soLuong;
        qlySPCT.soLuongTon(idSanPham, soLuongUpdate);
    }
