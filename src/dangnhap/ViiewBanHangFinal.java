    private void huyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_huyActionPerformed
        // TODO add your handling code here:
        int IDHoaDon = Integer.parseInt(tbHoaDon.getValueAt(tbHoaDon.getSelectedRow(), 0).toString());
        for (int i = 0; i < tbGioHang.getRowCount(); i++) {
            int soLuongGioHang = Integer.parseInt(tbGioHang.getValueAt(i, 2).toString());
            int IDsanPham = Integer.parseInt(tbGioHang.getValueAt(i, 0).toString());
            TraHang(IDHoaDon, IDsanPham, soLuongGioHang);
            HDCT_repo.traHang(IDHoaDon, IDsanPham);
        }
        showSanPham(qlySPCT.getSanPham());
        ShowgioHang(HDCT_repo.getGioHang(IDHoaDon));
        Hd_repo.traHang(IDHoaDon);
        showHoaDon(Hd_repo.getHDchuaThanhToan());
    }//GEN-LAST:event_huyActionPerformed

    private void ThemSanPhamActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ThemSanPhamActionPerformed
        String soLuong = String.valueOf(JOptionPane.showInputDialog("nhập số lượng"));
        if (checkChuoi(soLuong)) {
            int soLuongNhap = Integer.parseInt(soLuong);
            int soLuongTon;
            soLuongTon = Integer.parseInt(tbSanPham.getValueAt(tbSanPham.getSelectedRow(), 6).toString());
            if (soLuongNhap > soLuongTon) {
                JOptionPane.showMessageDialog(this, "nhập quá số lượng");
            } else {
                int idSPCT = Integer.parseInt(tbSanPham.getValueAt(tbSanPham.getSelectedRow(), 0).toString());
                int idHoaDon = 0;
                try {
                    idHoaDon = Integer.parseInt(tbHoaDon.getValueAt(tbHoaDon.getSelectedRow(), 0).toString());
                } finally {
                }
                int giaBan = Integer.parseInt(tbSanPham.getValueAt(tbSanPham.getSelectedRow(), 7).toString());
                int giamGia = 0;
                int soLuongConLai = soLuongTon - soLuongNhap;
                qlySPCT.soLuongTon(idSPCT, soLuongConLai);
                HoaDonChiTiet newHDCT = new HoaDonChiTiet(idHoaDon, idSPCT, soLuongNhap, giaBan, giamGia);
                if (HDCT_repo.getHoaDonChiTiet(idSPCT, idHoaDon).isEmpty()) {
                    HDCT_repo.addHoaDonCT(newHDCT);
                } else {
                    int soLuongGioHang = HDCT_repo.getHoaDonChiTiet(idSPCT, idHoaDon).get(0).getSoLuong();
                    int soLuongCuoi = soLuongNhap + soLuongGioHang;
                    HDCT_repo.soLuongTonGioHang(idHoaDon, idSPCT, soLuongCuoi);
                }
                ShowgioHang(HDCT_repo.getGioHang(idHoaDon));
                showSanPham(qlySPCT.getSanPham());
            }
            txtTongTien.setText(String.valueOf(tongTien()));
            tinhToanTien(tongTien());
        } else {
            JOptionPane.showMessageDialog(this, "số lượng phải là số");
        }
    }//GEN-LAST:event_ThemSanPhamActionPerformed

    private void traHangActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_traHangActionPerformed
        String soLuong = String.valueOf(JOptionPane.showInputDialog("nhập số lượng"));
        if (checkChuoi(soLuong)) {
            int soLuongNhap = Integer.parseInt(soLuong);
            int soLuongOGioHang;
            soLuongOGioHang = Integer.parseInt(tbGioHang.getValueAt(tbGioHang.getSelectedRow(), 2).toString());
            if (soLuongNhap > soLuongOGioHang) {
                JOptionPane.showMessageDialog(this, "nhập quá số lượng");
            } else {
                int idHoaDon = Integer.parseInt(tbHoaDon.getValueAt(tbHoaDon.getSelectedRow(), 0).toString());
                int idSPCT = Integer.parseInt(tbGioHang.getValueAt(tbGioHang.getSelectedRow(), 0).toString());
                for (int i = 0; i < tbSanPham.getRowCount(); i++) {
                    int idSanPham = Integer.parseInt(tbSanPham.getValueAt(i, 0).toString());
                    if (idSPCT == idSanPham) {
                        int soLuongton = Integer.parseInt(tbSanPham.getValueAt(i, 6).toString());
                        int soluongsanpham = soLuongton + soLuongNhap;
                        int soLuongoGioHangSaukhitra = soLuongOGioHang - soLuongNhap;
                        qlySPCT.soLuongTon(idSPCT, soluongsanpham);
                        HDCT_repo.soLuongTonGioHang(idHoaDon, idSPCT, soLuongoGioHangSaukhitra);
                        ShowgioHang(HDCT_repo.getGioHang(idHoaDon));
                        showSanPham(qlySPCT.getSanPham());
                        int tongTien = tongTien();
                        txtTongTien.setText(String.valueOf(tongTien));
                        tinhToanTien(tongTien());
                    }
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "số lượng phải là số");
        }        // TODO add your handling code here:
    }//GEN-LAST:event_traHangActionPerformed

