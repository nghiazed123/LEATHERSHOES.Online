       public void loadData() {
        dtbKh = (DefaultTableModel) tblKhachHang.getModel();
        dtbKh.setRowCount(0);
        khh = khRepo.getAll(); // Cập nhật danh sách nhân viên từ cơ sở dữ liệu
        for (khachHang a : khh) {
            Object[] n = new Object[5];
            n[0] = a.getIdKhachHang();
            n[1] = a.getTenKhachHang();
            n[2] = a.getSdt();
            n[3] = a.getDiaChi();
            n[4] = a.isTrangThai(); 
            dtbKh.addRow(n);
        }
        updateMaKhachHangField();
    }

    private void detailKhachHang(int index) {
        khachHang nv = khRepo.getAll().get(index);
        txtIDKH.setText(String.valueOf(nv.getIdKhachHang()));
        txtTenKH.setText(nv.getTenKhachHang());
        txtSdt.setText(nv.getSdt());
        txtDiaChi.setText(nv.getDiaChi());
        boolean trangThai = nv.isTrangThai();
        if (trangThai == true) {
            rdoAc.setSelected(true);
        } else {
            rdoIN.setSelected(true);
        }
    }

