    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        try {
            if (txtTenKH.getText().isEmpty() || txtSdt.getText().isEmpty() || txtDiaChi.getText().isEmpty() || txtSdt.getText() == null) {
                JOptionPane.showMessageDialog(this, "Không được bỏ trống dữ liệu");
                return;
            }
            if (!checkDuLieu()) {
                return;
            }
            if (!isValidPhoneNumber(txtSdt.getText())) {
                JOptionPane.showMessageDialog(this, "Số điện thoại không hợp lệ! "
                    + "\n Vui lòng nhập số điện thoại có độ dài 10 hoặc 11 chữ số "
                    + "\n Số điện thoại phải bắt đầu bằng 0.");
                return; // Exit the method if the phone number is invalid
            }

            khachHang kh = getFormData();
            if (khRepo.add(kh)) {
                loadData();
                JOptionPane.showMessageDialog(this, "Thêm nhân viên thành công");
                updateMaKhachHangField();
            } else {
                JOptionPane.showMessageDialog(this, "Thêm thất bại");
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Thêm thất bại");
        }
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        try {
            if (txtTenKH.getText().isEmpty() || txtSdt.getText().isEmpty() || txtDiaChi.getText().isEmpty() || txtSdt.getText() == null) {
                JOptionPane.showMessageDialog(this, "Không được bỏ trống dữ liệu");
                return;
            }
            if (!checkDuLieu()) {
                return;
            }
            if (!isValidPhoneNumber(txtSdt.getText())) {
                JOptionPane.showMessageDialog(this, "Số điện thoại không hợp lệ! "
                    + "\n Vui lòng nhập số điện thoại có độ dài 10 hoặc 11 chữ số "
                    + "\n Số điện thoại phải bắt đầu bằng 0.");
                return; // Exit the method if the phone number is invalid
            }

            if (tblKhachHang.getSelectedRow() == -1) {
                JOptionPane.showMessageDialog(this, "Chọn nhân viên cần sửa");
                return;
            }
            khachHang kh = getFormData();
            khRepo.Edit(kh);
            loadData();
            JOptionPane.showMessageDialog(this, "Sửa dữ liệu thành công");
            updateMaKhachHangField();
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Sửa thất bại");
        }
    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:

        try {
            int row = tblKhachHang.getSelectedRow();
            if (row == -1) {
                JOptionPane.showMessageDialog(this, "Chọn nhân viên cần xóa");
                return;
            }
            int manv = khh.get(row).getIdKhachHang();
            khachHang nv = new khachHang();
            nv.setIdKhachHang(manv);
            if (khRepo.Delete(nv) > 0) {
                loadData();
                JOptionPane.showMessageDialog(this, "Xóa dữ liệu thành công");

            } else {
                JOptionPane.showMessageDialog(this, "Xóa thất bại");
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Xóa thất bại");
        }
    }//GEN-LAST:event_btnXoaActionPerformed


